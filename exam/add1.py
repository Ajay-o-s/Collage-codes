num_1 = int(input("Enter 1 number : "));
num_2 = int(input("Enter 2 number : "));

add = num_1 + num_2;
sub = num_1 -num_2;
mul = num_1 * num_2;
div = num_1 / num_2;
lk = num_1 % num_2;
print("{0} + {1} = {2}".format(num_1,num_2,add));
print("{0} - {1} = {2}".format(num_1,num_2,sub));
print("{0} * {1} = {2}".format(num_1,num_2,mul));
print("{0} / {1} = {2}".format(num_1,num_2,div));
print("{0} % {1} = {2} , {3}".format(num_1,num_2,num_1%num_2,lk));
